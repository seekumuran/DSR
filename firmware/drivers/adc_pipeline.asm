NAME ADC_PIPELINE

PUBLIC SAMPLE_ALL_AXES

EXTRN CODE (ADC_READ)

EXTRN DATA (LX_VAL)
EXTRN DATA (LY_VAL)
EXTRN DATA (RX_VAL)
EXTRN DATA (RY_VAL)

SAMPLE_ALL_AXES:

    ACALL ADC_READ
    MOV LX_VAL, A

    ACALL ADC_READ
    MOV LY_VAL, A

    ACALL ADC_READ
    MOV RX_VAL, A

    ACALL ADC_READ
    MOV RY_VAL, A

    RET
