NAME JOYSTICK

PUBLIC READ_LX
PUBLIC READ_LY
PUBLIC READ_RX
PUBLIC READ_RY

PUBLIC LX_VAL
PUBLIC LY_VAL
PUBLIC RX_VAL
PUBLIC RY_VAL

LX_VAL DATA 50H
LY_VAL DATA 51H
RX_VAL DATA 52H
RY_VAL DATA 53H

READ_LX:
    MOV LX_VAL, #080H
    RET

READ_LY:
    MOV LY_VAL, #080H
    RET

READ_RX:
    MOV RX_VAL, #080H
    RET

READ_RY:
    MOV RY_VAL, #080H
    RET
