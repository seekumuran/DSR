NAME PRIORITY_SCHED

PUBLIC RUN_PRIORITY_SCHEDULER

TASK0_PRIORITY DATA 2BH
TASK1_PRIORITY DATA 2CH
TASK2_PRIORITY DATA 2DH

RUN_PRIORITY_SCHEDULER:

    MOV A, TASK0_PRIORITY

    CJNE A, TASK1_PRIORITY, CHECK_TASK2

    ACALL TASK_INPUT
    RET

CHECK_TASK2:

    CJNE A, TASK2_PRIORITY, RUN_DEFAULT

    ACALL TASK_PACKET
    RET

RUN_DEFAULT:

    ACALL TASK_UART

    RET
