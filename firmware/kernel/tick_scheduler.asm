NAME TICK_SCHED

PUBLIC INIT_TICK_SYSTEM
PUBLIC UPDATE_TICK_SYSTEM

SYS_TICK_L DATA 36H
SYS_TICK_H DATA 37H

INIT_TICK_SYSTEM:

    MOV SYS_TICK_L, #00H
    MOV SYS_TICK_H, #00H

    RET

UPDATE_TICK_SYSTEM:

    INC SYS_TICK_L

    CJNE SYS_TICK_L, #00H, EXIT_TICK

    INC SYS_TICK_H

EXIT_TICK:

    RET
