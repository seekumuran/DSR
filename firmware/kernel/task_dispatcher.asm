NAME TASK_DISPATCH

PUBLIC DISPATCH_TASK

CURRENT_TASK DATA 45H

DISPATCH_TASK:

    MOV A, CURRENT_TASK

    CJNE A, #00H, TASK1

    ACALL TASK_INPUT
    SJMP NEXT_TASK

TASK1:

    CJNE A, #01H, TASK2

    ACALL TASK_PACKET
    SJMP NEXT_TASK

TASK2:

    ACALL TASK_UART

NEXT_TASK:

    INC CURRENT_TASK

    CJNE CURRENT_TASK, #03H, EXIT_DISPATCH

    MOV CURRENT_TASK, #00H

EXIT_DISPATCH:

    RET
