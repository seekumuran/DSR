NAME TASK_STATES

PUBLIC INIT_TASK_STATES
PUBLIC SET_TASK_READY
PUBLIC SET_TASK_RUNNING
PUBLIC SET_TASK_SLEEP

TASK0_STATE DATA 28H
TASK1_STATE DATA 29H
TASK2_STATE DATA 2AH

READY_STATE   EQU 01H
RUNNING_STATE EQU 02H
SLEEP_STATE   EQU 03H

INIT_TASK_STATES:

    MOV TASK0_STATE, #READY_STATE
    MOV TASK1_STATE, #READY_STATE
    MOV TASK2_STATE, #READY_STATE

    RET

SET_TASK_READY:

    MOV @R0, #READY_STATE

    RET

SET_TASK_RUNNING:

    MOV @R0, #RUNNING_STATE

    RET

SET_TASK_SLEEP:

    MOV @R0, #SLEEP_STATE

    RET
