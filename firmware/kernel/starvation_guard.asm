NAME STARVATION

PUBLIC CHECK_STARVATION

TASK0_STARVE DATA 3BH
TASK1_STARVE DATA 3CH
TASK2_STARVE DATA 3DH

CHECK_STARVATION:

    INC TASK0_STARVE
    INC TASK1_STARVE
    INC TASK2_STARVE

    CJNE TASK0_STARVE, #0FFH, CHECK_TASK1

    MOV TASK0_STARVE, #00H

CHECK_TASK1:

    CJNE TASK1_STARVE, #0FFH, CHECK_TASK2

    MOV TASK1_STARVE, #00H

CHECK_TASK2:

    CJNE TASK2_STARVE, #0FFH, EXIT_STARVE

    MOV TASK2_STARVE, #00H

EXIT_STARVE:

    RET
