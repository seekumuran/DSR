NAME RING_BUFFER

PUBLIC BUFFER_PUSH
PUBLIC BUFFER_POP

BUFFER_HEAD DATA 32H
BUFFER_TAIL DATA 33H

BUFFER_BASE EQU 80H

BUFFER_PUSH:

    MOV R0, BUFFER_HEAD

    ADD A, #BUFFER_BASE

    MOV R1, A

    MOV @R1, B

    INC BUFFER_HEAD

    ANL BUFFER_HEAD, #0FH

    RET

BUFFER_POP:

    MOV A, BUFFER_TAIL

    ADD A, #BUFFER_BASE

    MOV R0, A

    MOV A, @R0

    INC BUFFER_TAIL

    ANL BUFFER_TAIL, #0FH

    RET
