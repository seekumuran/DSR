NAME SCHEDULER

PUBLIC INIT_SCHEDULER
PUBLIC RUN_SCHEDULER

TASK_INDEX DATA 30H

INIT_SCHEDULER:
    MOV TASK_INDEX, #00H
    RET

RUN_SCHEDULER:

    MOV A, TASK_INDEX

    CJNE A, #00H, CHECK_TASK1
    ACALL TASK_INPUT
    SJMP NEXT_TASK

CHECK_TASK1:
    CJNE A, #01H, CHECK_TASK2
    ACALL TASK_PACKET
    SJMP NEXT_TASK

CHECK_TASK2:
    ACALL TASK_UART

NEXT_TASK:
    INC TASK_INDEX

    CJNE TASK_INDEX, #03H, EXIT_SCHED
    MOV TASK_INDEX, #00H

EXIT_SCHED:
    RET
