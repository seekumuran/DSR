NAME DSSX_PACKET

PUBLIC BUILD_PACKET

BUILD_PACKET:

    MOV 70H, #0AAH
    MOV 71H, #01H
    MOV 72H, DEVICE_ID_REG

    MOV 73H, 42H
    MOV 74H, 43H

    MOV 75H, LX_VAL
    MOV 76H, LY_VAL

    MOV 77H, RX_VAL
    MOV 78H, RY_VAL

    ACALL CRC16_COMPUTE

    MOV 79H, CRC_H
    MOV 7AH, CRC_L

    RET
