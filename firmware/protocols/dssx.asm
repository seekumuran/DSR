NAME DSSX

PUBLIC BUILD_PACKET

EXTRN DATA (BUTTON_STATE1)
EXTRN DATA (BUTTON_STATE2)

EXTRN DATA (LX_VAL)
EXTRN DATA (LY_VAL)
EXTRN DATA (RX_VAL)
EXTRN DATA (RY_VAL)

PACKET_BUFFER DATA 70H

BUILD_PACKET:

    MOV 70H, #0AAH
    MOV 71H, #01H
    MOV 72H, #01H

    MOV 73H, BUTTON_STATE1
    MOV 74H, BUTTON_STATE2

    MOV 75H, LX_VAL
    MOV 76H, LY_VAL
    MOV 77H, RX_VAL
    MOV 78H, RY_VAL

    ACALL CRC16_CALC

    MOV 79H, CRC16_H
    MOV 7AH, CRC16_L

    RET
