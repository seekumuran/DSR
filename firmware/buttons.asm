READ_BUTTONS:

    MOV A, #00H

CHECK_UP:
    JB UP_BTN, CHECK_DOWN
    ORL A, #01H

CHECK_DOWN:
    JB DOWN_BTN, CHECK_LEFT
    ORL A, #02H

CHECK_LEFT:
    JB LEFT_BTN, CHECK_RIGHT
    ORL A, #04H

CHECK_RIGHT:
    JB RIGHT_BTN, CHECK_A
    ORL A, #08H

CHECK_A:
    JB A_BTN, CHECK_B
    ORL A, #10H

CHECK_B:
    JB B_BTN, CHECK_X
    ORL A, #20H

CHECK_X:
    JB X_BTN, CHECK_Y
    ORL A, #40H

CHECK_Y:
    JB Y_BTN, END_BUTTONS
    ORL A, #80H

END_BUTTONS:
    RET
