NAME INTERRUPTS

PUBLIC INIT_INTERRUPTS
PUBLIC TIMER0_ISR

ORG 000BH
LJMP TIMER0_ISR

INIT_INTERRUPTS:

    MOV TMOD, #01H

    MOV TH0, #0FCH
    MOV TL0, #066H

    SETB ET0
    SETB EA
    SETB TR0

    RET

TIMER0_ISR:

    CLR TR0

    MOV TH0, #0FCH
    MOV TL0, #066H

    ACALL RUN_SCHEDULER

    SETB TR0

    RETI
